// Functie om diploma's toe te voegen aan een bemanningslid
const addDiplomasToCrewMember = (crewMember: any, crewId: string) => {
  const diplomaString = diplomaMapping[crewId]
  if (diplomaString) {
    crewMember.diplomas = convertDiplomaAbbreviations(diplomaString)
  } else {
    crewMember.diplomas = []
  }
  
  // Voeg ontbrekende velden toe als ze niet bestaan
  if (!crewMember.assignmentHistory) crewMember.assignmentHistory = []
  if (!crewMember.vasteDienst) crewMember.vasteDienst = false
  if (!crewMember.inDienstVanaf) crewMember.inDienstVanaf = null
  
  return crewMember
}

// Voeg diploma's toe aan bestaande bemanningsleden
const addDiplomasToExistingCrew = (crewDatabase: any) => {
  Object.keys(crewDatabase).forEach(crewId => {
    if (crewDatabase[crewId] && diplomaMapping[crewId]) {
      crewDatabase[crewId].diplomas = convertDiplomaAbbreviations(diplomaMapping[crewId])
    } else if (crewDatabase[crewId]) {
      crewDatabase[crewId].diplomas = []
    }
  })
  return crewDatabase
}

