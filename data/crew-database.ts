const MS_PER_DAY = 24 * 60 * 60 * 1000;
const today = new Date();
Object.values(crewDatabase).forEach((crew: any) => {
  if (crew.status === "thuis" && !crew.onBoardSince) {
    let daysAgo = 14;
    if (crew.regime === "1/1") daysAgo = 7;
    if (crew.regime === "3/3") daysAgo = 21;
    const date = new Date(today.getTime() - daysAgo * MS_PER_DAY);
    crew.onBoardSince = date.toISOString().split("T")[0];
  }
});

Object.values(crewDatabase).forEach((crew: any) => {
  if (!crew.assignmentHistory) {
    crew.assignmentHistory = [];
    if (crew.shipId && crew.onBoardSince) {
      crew.assignmentHistory.push({
        date: crew.onBoardSince,
        shipId: crew.shipId,
        action: crew.status === "aan-boord" ? "toegewezen" : "thuis",
        note: "Initiële toewijzing"
      });
    }
  }
});

